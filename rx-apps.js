function getRxApp(slug) {
  return RX_APPS.find((app) => app.slug === slug);
}

function getAppleDevicePlatform() {
  const { userAgent = "", platform = "", maxTouchPoints = 0 } = navigator;
  const uaPlatform = navigator.userAgentData?.platform || platform;
  const normalized = `${userAgent} ${uaPlatform}`.toLowerCase();

  if (
    normalized.includes("ipad") ||
    (platform === "MacIntel" && maxTouchPoints > 1)
  ) {
    return "ipad";
  }

  if (normalized.includes("iphone") || normalized.includes("ipod")) {
    return "iphone";
  }

  if (normalized.includes("mac")) {
    return "mac";
  }

  return null;
}

function getAppleDevicePlatformLabel(platform) {
  if (platform === "mac") return "Mac";
  if (platform === "ipad") return "iPad";
  if (platform === "iphone") return "iPhone";
  return "App Store";
}

function getAppleDevicePlatformFamily() {
  const platform = getAppleDevicePlatform();
  return platform === "mac" ? "mac" : "ios";
}

function getPlatformAwareAppUrl(app) {
  if (!app?.url) return "#";

  const platform = getAppleDevicePlatform();

  try {
    const url = new URL(app.url);

    if (platform) {
      url.searchParams.set("platform", platform);
    }

    return url.toString();
  } catch {
    return app.url;
  }
}

function getRxAppView(app, platformFamily = getAppleDevicePlatformFamily()) {
  const details = app?.platformDetails?.[platformFamily];

  if (!details) {
    return app;
  }

  return {
    ...app,
    ...details,
  };
}

function getDisplayTags(app) {
  const tags = [...(app?.tags || [])];
  const supportsMac =
    app?.platforms?.includes("Mac") || app?.platformDetails?.mac;

  if (supportsMac && !tags.includes("Mac")) {
    const insertAt = Math.min(2, tags.length);
    tags.splice(insertAt, 0, "Mac");
  }

  return tags;
}
